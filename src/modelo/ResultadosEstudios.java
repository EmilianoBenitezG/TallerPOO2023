package modelo;

public class ResultadosEstudios {
	
	private String fecha;
	private String hora;
	private String tipoEstudio;
	private String informeMedico;
	
	public ResultadosEstudios() {
	}
	
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	public String getTipoEstudio() {
		return tipoEstudio;
	}
	public void setTipoEstudio(String tipoEstudio) {
		this.tipoEstudio = tipoEstudio;
	}
	public String getInformeMedico() {
		return informeMedico;
	}
	public void setInformeMedico(String informeMedico) {
		this.informeMedico = informeMedico;
	}
	
}
